-- Trigger: actualiza coste en función del coste del artista, cuando se asocia un artista a la actividad
DELIMITER //
CREATE TRIGGER actualiza_coste_actividad AFTER INSERT ON actividad_artista
	FOR EACH ROW
	BEGIN
		DECLARE v_coste_artista DECIMAL(18,2);
        
        SELECT a.cache INTO v_coste_artista FROM swellevents.artista a WHERE a.id_artista = NEW.id_artista;
        
		UPDATE swellevents.actividad act SET act.coste = act.coste + v_coste_artista WHERE act.id_actividad = NEW.id_actividad;
	END;//
DELIMITER ;
